<h1>Origen de Protein Mix</h1>
<div id="lineaDelgada"></div>
<p>
Actualmente, nuestro estilo de vida nos impide mantener una alimentación correcta debido a extensos horarios de trabajo o de escuela, comemos a diferentes horas e incluso la mala elección de nuestros alimentos (comida rápida) generan que no se tenga un aporte adecuado de macronutrientes (proteínas, carbohidratos y grasas).
Las proteínas son de gran valor biológico en nuestro cuerpo debido a que nos ayudan a construir los tejidos del cuerpo (músculos, sangre, piel, huesos), formar defensas contra las enfermedades, asegurar el buen funcionamiento del organismo, así como proporcionar energía.
Es por ello, que los productos de Protein Mix son una opción saludable, con gran sabor y ricos en proteínas que nos ayudan a satisfacer nuestros requerimientos diarios de este macronutriente, además de que son fáciles de llevar a cualquier lugar y evitar así el consumo de comida chatarra. </p>


<h1>Misión</h1>
<div id="lineaDelgada"></div>

<p>Ofrecer suplementos alimenticios que contengan la concentración adecuada para impactar de manera positiva en la salud de la persona que los consume a un precio accesible. Así como brindar asesoría nutricional en padecimientos crónico degenerativos a un costo accesible y gratuito en personas con cáncer.</p>

